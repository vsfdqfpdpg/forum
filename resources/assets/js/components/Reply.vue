<template>
    <div :id="'reply-'+id" class="panel" :class="isBest ? 'panel-success' : 'panel-default'">
        <div class="panel-heading">
            <div class="level">
                <h5 class="flex">
                    <a :href="'/profiles/'+data.owner.name" v-text="data.owner.name"></a>
                    said <span v-text="ago"></span>
                </h5>
                <div v-if="signedIn">
                    <favorite :reply="data"></favorite>
                </div>
            </div>
        </div>
        <div class="panel-body">
            <div v-if="editing">
                <form @submit="update">
                    <div class="form-group">
                        <textarea name="" id="" class="form-control" v-model="body" required></textarea>
                    </div>
                    <button class="btn btn-xs btn-primary" type="submit">Update</button>
                    <button class="btn btn-xs btn-link" @click="editing = false" type="button">Cancel</button>
                </form>
            </div>
            <div v-else v-html="body"></div>
        </div>
        <div class="panel-footer level">
            <div v-if="authorize('updateReply',data)">
                <button class="btn btn-xs mr-1" @click="editing = true">Edit</button>
                <button class="btn btn-xs btn-danger mr-1" @click="destroy">Delete</button>
            </div>
            <button class="btn btn-xs btn-default ml-a" @click="markBestReply" v-show="! isBest">Best Reply?</button>
        </div>
    </div>
</template>
<script>
    import Favorite from './Favorite';
    import moment from 'moment';
    export default {
        props: ['data'],
        components: {Favorite},
        data() {
            return {editing: false, body: this.data.body, id: this.data.id, isBest : this.data.isBest}
        },
        computed: {
            ago(){
                return moment(this.data.created_at).fromNow() + '...';
            }
        },
        created(){
            window.events.$on('best-reply-selected',id=>{
                this.isBest = ( id == this.id);
            });
        },
        methods: {
            update() {
                axios.patch('/replies/' + this.data.id, {
                    body: this.body
                }).catch( error =>{
                    flash(error.response.data,'danger');
                });

                this.editing = false;
                flash('Updated!');
            },
            destroy() {
                axios.delete('/replies/' + this.data.id);
                this.$emit('deleted', this.data.id);
            },
            markBestReply(){
                axios.post('/replies/'+this.data.id+'/best');
                window.event.$emit('best-reply-selected',this.data.id);
            }
        }
    }
</script>